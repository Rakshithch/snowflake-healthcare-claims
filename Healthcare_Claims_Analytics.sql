-- ============================================
-- Step 4: Analytics Views
-- ============================================

-- View 1: Claims summary by payer type
CREATE OR REPLACE VIEW VW_CLAIMS_BY_PAYER AS
SELECT
    PAYER_TYPE,
    COUNT(CLAIM_ID)         AS TOTAL_CLAIMS,
    SUM(PAID_AMOUNT)        AS TOTAL_PAID,
    AVG(PAID_AMOUNT)        AS AVG_CLAIM_AMOUNT,
    COUNT(CASE WHEN CLAIM_STATUS = 'DENIED' THEN 1 END) AS DENIED_CLAIMS,
    ROUND(COUNT(CASE WHEN CLAIM_STATUS = 'DENIED' THEN 1 END) * 100.0 / COUNT(CLAIM_ID), 2) AS DENIAL_RATE_PCT
FROM RAW_CLAIMS
GROUP BY PAYER_TYPE;

-- View 2: Claims by diagnosis code
CREATE OR REPLACE VIEW VW_CLAIMS_BY_DIAGNOSIS AS
SELECT
    DIAGNOSIS_CODE,
    COUNT(CLAIM_ID)         AS TOTAL_CLAIMS,
    SUM(PAID_AMOUNT)        AS TOTAL_PAID,
    AVG(PAID_AMOUNT)        AS AVG_PAID,
    CLAIM_STATUS
FROM RAW_CLAIMS
GROUP BY DIAGNOSIS_CODE, CLAIM_STATUS
ORDER BY TOTAL_CLAIMS DESC;

-- View 3: Monthly claims trend
CREATE OR REPLACE VIEW VW_MONTHLY_TREND AS
SELECT
    DATE_TRUNC('MONTH', CLAIM_DATE) AS CLAIM_MONTH,
    COUNT(CLAIM_ID)                 AS TOTAL_CLAIMS,
    SUM(PAID_AMOUNT)                AS TOTAL_PAID,
    AVG(PAID_AMOUNT)                AS AVG_PAID
FROM RAW_CLAIMS
GROUP BY CLAIM_MONTH
ORDER BY CLAIM_MONTH;

-- Step 5: Query the views
SELECT * FROM VW_CLAIMS_BY_PAYER;
SELECT * FROM VW_CLAIMS_BY_DIAGNOSIS;
SELECT * FROM VW_MONTHLY_TREND;

-- ============================================
-- Step 6: Star Schema (Data Warehouse Layer)
-- ============================================

-- Dimension: Providers
CREATE OR REPLACE TABLE DIM_PROVIDER (
    PROVIDER_KEY    NUMBER AUTOINCREMENT PRIMARY KEY,
    PROVIDER_ID     VARCHAR(20),
    PROVIDER_NAME   VARCHAR(100),
    SPECIALTY       VARCHAR(50),
    STATE           VARCHAR(2)
);

-- Dimension: Patients
CREATE OR REPLACE TABLE DIM_PATIENT (
    PATIENT_KEY     NUMBER AUTOINCREMENT PRIMARY KEY,
    PATIENT_ID      VARCHAR(20),
    AGE_GROUP       VARCHAR(20),
    STATE           VARCHAR(2)
);

-- Dimension: Date
CREATE OR REPLACE TABLE DIM_DATE (
    DATE_KEY        DATE PRIMARY KEY,
    YEAR            NUMBER,
    MONTH           NUMBER,
    MONTH_NAME      VARCHAR(10),
    QUARTER         NUMBER
);

-- Fact Table: Claims
CREATE OR REPLACE TABLE FACT_CLAIMS (
    CLAIM_ID        VARCHAR(20) PRIMARY KEY,
    PATIENT_ID      VARCHAR(20),
    PROVIDER_ID     VARCHAR(20),
    CLAIM_DATE      DATE,
    DIAGNOSIS_CODE  VARCHAR(10),
    PROCEDURE_CODE  VARCHAR(10),
    PAID_AMOUNT     DECIMAL(10,2),
    CLAIM_STATUS    VARCHAR(20),
    PAYER_TYPE      VARCHAR(30)
);

