-- Final analytical query joining all layers
SELECT
    p.PROVIDER_NAME,
    p.SPECIALTY,
    pt.AGE_GROUP,
    d.MONTH_NAME,
    f.PAYER_TYPE,
    COUNT(f.CLAIM_ID)   AS TOTAL_CLAIMS,
    SUM(f.PAID_AMOUNT)  AS TOTAL_PAID,
    AVG(f.PAID_AMOUNT)  AS AVG_PAID
FROM FACT_CLAIMS f
JOIN DIM_PROVIDER p  ON f.PROVIDER_ID = p.PROVIDER_ID
JOIN DIM_PATIENT  pt ON f.PATIENT_ID  = pt.PATIENT_ID
JOIN DIM_DATE     d  ON f.CLAIM_DATE  = d.DATE_KEY
GROUP BY p.PROVIDER_NAME, p.SPECIALTY, pt.AGE_GROUP, d.MONTH_NAME, f.PAYER_TYPE
ORDER BY TOTAL_PAID DESC;